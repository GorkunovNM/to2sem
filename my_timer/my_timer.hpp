#ifndef MY_TIMER_HPP
#define MY_TIMER_HPP

///////////////////////////////////////////////////////////////////////////////
/// \namespace my_timer
///////////////////////////////////////////////////////////////////////////////
namespace my_timer {
    ///////////////////////////////////////////////////////////////////////////
    /// \private start_time - start time.
    /// \class   simple_timer my_timer.hpp "my_timer/my_timer.hpp"
    /// \brief   Simple timer.
    ///////////////////////////////////////////////////////////////////////////
    class simple_timer {
    public:
        ///////////////////////////////////////////////////////////////////////
        /// \param  -
        /// \return -
        /// \brief  Sets start_time.
        ///////////////////////////////////////////////////////////////////////
        void start();

        ///////////////////////////////////////////////////////////////////////
        /// \param  -
        /// \return Elapsed time since start_time.
        /// \brief  Returns the elapsed time since start_time.
        ///////////////////////////////////////////////////////////////////////
        double finish();

    private:
        std::chrono::time_point<std::chrono::system_clock> start_time;
    };
}

#endif
